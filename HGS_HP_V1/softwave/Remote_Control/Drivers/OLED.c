uint8_t OLED_GRAM[128][8];	
//开启OLED显示    
void OLED_Display_On(void)
{
	LCD_WrCmd(0X8D);  //SET DCDC命令
	LCD_WrCmd(0X14);  //DCDC ON
	LCD_WrCmd(0XAF);  //DISPLAY ON
}
//关闭OLED显示     
void OLED_Display_Off(void)
{
	LCD_WrCmd(0X8D);  //SET DCDC命令
	LCD_WrCmd(0X10);  //DCDC OFF
	LCD_WrCmd(0XAE);  //DISPLAY OFF
}

//更新显存到LCD		 
void OLED_Refresh_Gram(void)
{
	uint8_t i,n;		    
	for(i=0;i<8;i++)  
	{  
		LCD_WrCmd (0xb0+i);    //设置页地址（0~7）
		LCD_WrCmd (0x00);      //设置显示位置―列低地址
		LCD_WrCmd (0x10);      //设置显示位置―列高地址   
		for(n=0;n<128;n++)LCD_WrDat(OLED_GRAM[n][i]); 
	}   
}


//清屏函数,清完屏,整个屏幕是黑色的!和没点亮一样!!!	  
void OLED_Clear(void)  
{  
	uint8_t i,n;  
	for(i=0;i<8;i++)for(n=0;n<128;n++)OLED_GRAM[n][i]=0X00;  
	OLED_Refresh_Gram();//更新显示
}
//画点 
//x:0~127
//y:0~63
//t:1 填充 0,清空				   
void OLED_DrawPoint(uint8_t x,uint8_t y,uint8_t t)
{
	uint8_t pos,bx,temp=0;
	if(x>127||y>63)return;//超出范围了.
	pos=7-y/8;
	bx=y%8;
	temp=1<<(7-bx);
	if(t)OLED_GRAM[x][pos]|=temp;
	else OLED_GRAM[x][pos]&=~temp;	    
}
//x1,y1,x2,y2 填充区域的对角坐标
//确保x1<=x2;y1<=y2 0<=x1<=127 0<=y1<=63	 	 
//dot:0,清空;1,填充	  
void OLED_Fill_All(uint8_t x1,uint8_t y1,uint8_t x2,uint8_t y2,uint8_t dot)  
{  
	uint8_t x,y;  
	for(x=x1;x<=x2;x++)
	{
		for(y=y1;y<=y2;y++)OLED_DrawPoint(x,y,dot);
	}													    
	OLED_Refresh_Gram();//更新显示
}


